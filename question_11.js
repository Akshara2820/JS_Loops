// For loop

for (i=1; i<=20; i++){
    if (i%3==0 && i%7==0){
        console.log("Navgurukul")
    }
    else if (i%3==0){
        console.log("Nav")
    }
    else if (i%7==0){
        console.log("Gurukul")
    }
    else{
        console.log(i)
    }
}

// while loop

i=0
while (i<=20){
    if (i%3==0 && i%7==0){
        console.log("Navgurukul")
    }
    else if (i%3==0){
        console.log("Nav")
    }
    else if (i%7==0){
        console.log("Gurukul")
    }
    else{
        console.log(i)
    }
    i++
}

